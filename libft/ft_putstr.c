#include "libft.h"

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	if (str == 0)
	{
		write(1, "String is empty\n", 16);
		return ;
	}
	while(str[i] != 0)
	{
		write(1, &str[i], 1);
		i++;
	}
}
